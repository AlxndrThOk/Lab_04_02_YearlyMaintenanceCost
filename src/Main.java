public class Main {
    public static void main(String[] args)
    {
        double springMaintenanceCosts = 100.10;
        double summerMaintenanceCosts = 100.20;
        double fallMaintenanceCosts = 100.30;
        double winterMaintenanceCosts = 100.40;
        double totalMaintenanceCosts = springMaintenanceCosts + summerMaintenanceCosts + fallMaintenanceCosts + winterMaintenanceCosts;
        System.out.println("Spring maintenance costs are $" + springMaintenanceCosts);
        System.out.println("Summer maintenance costs are $" + summerMaintenanceCosts);
        System.out.println("Fall maintenance costs are $" + fallMaintenanceCosts);
        System.out.println("Winter maintenance costs are $" + winterMaintenanceCosts);
        System.out.println("Total maintenance costs are $" + totalMaintenanceCosts);
    }
}